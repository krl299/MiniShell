#include "libft.h"

long int	ft_atol(const char *s)
{
	long int	n;
	int			sign;
	int			c;

	n = 0;
	sign = 1;
	c = 0;
	while ((s[c] == ' ') || (s[c] == '\f') || (s[c] == '\t')
		|| (s[c] == '\r') || (s[c] == '\n') || (s[c] == '\r'))
		c++;
	if ((s[c] == '-') || (s[c] == '+'))
	{
		if (s[c] == '-')
			sign *= -1;
		c++;
	}
	while (s[c] >= '0' && s[c] <= '9')
	{
		n = n * 10 + (s[c] - '0');
		c++;
	}
	return (sign * n);
}
